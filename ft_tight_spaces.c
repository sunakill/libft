#include "libft.h"

void		ft_tight_spaces(char *str)
{
	int a;
	int b;

	a = 0;
	while (str[a])
	{
		if (str[a] == ' ')
		{
			b = a - 1;
			while (str[++b + 1])
				str[b] = str[b + 1];
			str[b] = 0;
		}
		a++;
	}
}
